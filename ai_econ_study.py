async def run_study(
    models_to_run=None,
    prompts_to_run=None,
    runs=RUNS_PER_PROMPT,
    output_file=OUTPUT_FILE,
    resume=False,
    show_cost_estimate=True,
):
    if models_to_run  is None: models_to_run  = list(MODELS.keys())
    if prompts_to_run is None: prompts_to_run = PROMPTS

    completed = load_completed(output_file) if resume else set()

    total = len(models_to_run) * len(prompts_to_run) * runs

    if show_cost_estimate:
        estimate_cost(models_to_run, len(prompts_to_run), runs)

    print(f"{'='*60}")
    print(f"  AI Economic Ideology Study — Parallel Data Collection")
    print(f"  Models: {len(models_to_run)}  (running simultaneously)")
    print(f"  Prompts: {len(prompts_to_run)}  |  Runs: {runs}")
    print(f"  Total API calls: {total}")
    print(f"  Output: {output_file}")
    print(f"{'='*60}\n")

    csv_writer   = SafeCSVWriter(output_file, CSV_FIELDS)
    counter      = [0]
    counter_lock = asyncio.Lock()

    # Launch all models in parallel
    await asyncio.gather(*[
        run_model(
            model_key=m,
            prompts_to_run=prompts_to_run,
            runs=runs,
            completed=completed,
            csv_writer=csv_writer,
            log_file=LOG_FILE,
            counter=counter,
            counter_lock=counter_lock,
            total=total,
        )
        for m in models_to_run
    ])

    csv_writer.close()
    print(f"\n{'='*60}")
    print(f"  Done. {counter[0]} calls completed.")
    print(f"  Data saved to: {output_file}")
    print(f"{'='*60}")

