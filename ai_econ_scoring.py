async def run_coding(
    input_file="study_data_clean.csv",
    output_file="study_data_coded.csv",
    sample_only=False,
    concurrency=3,
):
    df = pd.read_csv(input_file)

    # Filter to clean rows with actual responses
    df = df[df['response_text'].notna() & (df['response_text'].astype(str).str.strip() != '')]
    df = df.reset_index(drop=True)

    if sample_only:
        # 2 rows per model per scenario_type = ~16 rows for validation
        df = df.groupby(['model_key','scenario_type']).head(2).reset_index(drop=True)
        print(f"SAMPLE MODE: coding {len(df)} rows")
    else:
        print(f"FULL MODE: coding {len(df)} rows")

    output_fields = [
        # Original study fields
        "row_id", "model_key", "model_display", "prompt_id",
        "scenario_type", "framing", "run_number",
        "response_length_chars", "tokens_in", "tokens_out",
        # Coding fields
        "ownership", "market_role", "redistribution",
        "individual_collective", "state_intervention",
        "composite_score", "ideology_label", "rationale",
        "coding_error",
        # Keep response for human validation
        "response_text",
    ]

    writer = SafeWriter(output_file, output_fields)
    client = anthropic.AsyncAnthropic(api_key=os.environ["ANTHROPIC_API_KEY"])
    semaphore = asyncio.Semaphore(concurrency)

    counter = [0]
    counter_lock = asyncio.Lock()
    total = len(df)

    async def _process_row(idx, row):
        scores = await score_response(
            client=client,
            row_id=idx,
            response_text=str(row['response_text']),
            prompt_id=row['prompt_id'],
            scenario_type=row['scenario_type'],
            semaphore=semaphore,
        )

        out_row = {
            "row_id":               idx,
            "model_key":            row['model_key'],
            "model_display":        row['model_display'],
            "prompt_id":            row['prompt_id'],
            "scenario_type":        row['scenario_type'],
            "framing":              row['framing'],
            "run_number":           row['run_number'],
            "response_length_chars": row.get('response_length_chars', ''),
            "tokens_in":            row.get('tokens_in', ''),
            "tokens_out":           row.get('tokens_out', ''),
            "ownership":            scores.get('ownership'),
            "market_role":          scores.get('market_role'),
            "redistribution":       scores.get('redistribution'),
            "individual_collective": scores.get('individual_collective'),
            "state_intervention":   scores.get('state_intervention'),
            "composite_score":      scores.get('composite_score'),
            "ideology_label":       scores.get('ideology_label'),
            "rationale":            scores.get('rationale'),
            "coding_error":         scores.get('error', ''),
            "response_text":        str(row['response_text'])[:500],  # truncate for readability
        }

        writer.write(out_row)

        async with counter_lock:
            counter[0] += 1
            done = counter[0]

        status = "✓" if not scores.get('error') else "✗"
        label = scores.get('ideology_label', 'ERROR')
        score = scores.get('composite_score', '')
        print(f"  [{done:>3}/{total}] {status} {str(row['model_key']):<8} | "
      f"{str(row['prompt_id']):<4} run {str(row['run_number'])} | "
      f"{str(label):<12} ({str(score)})")

        await asyncio.sleep(1.5)  # gentle rate limiting

    tasks = [_process_row(idx, row) for idx, row in df.iterrows()]
    await asyncio.gather(*tasks)

    writer.close()

    # ── Summary stats ──────────────────────────────────────────────────────
    coded = pd.read_csv(output_file)
    clean = coded[coded['coding_error'].isna() | (coded['coding_error'] == '')]

    print(f"\n{'='*60}")
    print(f"  Coding complete: {len(clean)}/{total} rows scored successfully")
    print(f"  Errors: {total - len(clean)}")
    print(f"\n  === Ideology label distribution ===")
    print(clean.groupby(['model_key','ideology_label']).size().unstack(fill_value=0).to_string())
    print(f"\n  === Mean composite score by model ===")
    print(clean.groupby('model_key')['composite_score'].agg(['mean','std','min','max']).round(3).to_string())
    print(f"\n  === Mean scores by dimension by model ===")
    dims = ['ownership','market_role','redistribution','individual_collective','state_intervention']
    print(clean.groupby('model_key')[dims].mean().round(3).to_string())
    print(f"{'='*60}")
    print(f"\n  Output saved to: {output_file}")

    return coded

