# ══════════════════════════════════════════════════════════════════════════════
# COST ESTIMATOR
# ══════════════════════════════════════════════════════════════════════════════

def estimate_cost(models_to_run, num_prompts, runs):
    """Print estimated cost based on real observed token usage."""
    avg_in  = 112   # observed tokens in per call
    avg_out = 350   # capped by MAX_TOKENS (was 753 before cap)
    calls_per_model = num_prompts * runs
    print(f"\n{'─'*50}")
    print(f"  Cost estimate (MAX_TOKENS={MAX_TOKENS})")
    print(f"  {calls_per_model} calls × {len(models_to_run)} models = "
          f"{calls_per_model * len(models_to_run)} total calls")
    print(f"{'─'*50}")
    total = 0.0
    for m in models_to_run:
        p = PRICING[m]
        cost_in  = (avg_in  * calls_per_model / 1_000_000) * p["input"]
        cost_out = (avg_out * calls_per_model / 1_000_000) * p["output"]
        subtotal = cost_in + cost_out
        total += subtotal
        print(f"  {MODELS[m]['display']:<30} ${subtotal:.2f}")
    print(f"{'─'*50}")
    print(f"  {'TOTAL':<30} ${total:.2f}  (±20%)")
    print(f"{'─'*50}\n")


