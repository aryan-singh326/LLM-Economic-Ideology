async def call_openai_async(prompt_text, model_id):
    client = openai.AsyncOpenAI(api_key=os.environ["OPENAI_API_KEY"])
    response = await client.chat.completions.create(
        model=model_id,
        messages=[{"role": "user", "content": prompt_text}],
        temperature=TEMPERATURE,
        max_tokens=MAX_TOKENS,
    )
    text = response.choices[0].message.content.strip()
    tokens_in  = response.usage.prompt_tokens
    tokens_out = response.usage.completion_tokens
    return text, tokens_in, tokens_out


async def call_anthropic_async(prompt_text, model_id):
    client = anthropic.AsyncAnthropic(api_key=os.environ["ANTHROPIC_API_KEY"])
    response = await client.messages.create(
        model=model_id,
        max_tokens=MAX_TOKENS,
        temperature=TEMPERATURE,
        messages=[{"role": "user", "content": prompt_text}],
    )
    text = response.content[0].text.strip()
    tokens_in  = response.usage.input_tokens
    tokens_out = response.usage.output_tokens
    return text, tokens_in, tokens_out


async def call_google_async(prompt_text, model_id):
    # google-genai doesn't have a native async client; run in thread pool
    loop = asyncio.get_event_loop()

    def _call():
        client = genai.Client(api_key=os.environ["GOOGLE_API_KEY"])
        response = client.models.generate_content(
            model=model_id,
            contents=prompt_text,
            config=genai_types.GenerateContentConfig(
                temperature=TEMPERATURE,
                max_output_tokens=MAX_TOKENS,
            )
        )
        text = response.text.strip()
        # Google returns token counts in usage_metadata
        tokens_in  = getattr(response.usage_metadata, "prompt_token_count",  0)
        tokens_out = getattr(response.usage_metadata, "candidates_token_count", 0)
        return text, tokens_in, tokens_out

    return await loop.run_in_executor(None, _call)


async def call_model_async(model_key, prompt_text):
    cfg = MODELS[model_key]
    if cfg["provider"] == "openai":
        return await call_openai_async(prompt_text, cfg["model_id"])
    elif cfg["provider"] == "anthropic":
        return await call_anthropic_async(prompt_text, cfg["model_id"])
    elif cfg["provider"] == "google":
        return await call_google_async(prompt_text, cfg["model_id"])
    else:
        raise ValueError(f"Unknown provider: {cfg['provider']}")


