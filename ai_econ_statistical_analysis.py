def fig1_composite_distributions(df):
    """Figure 1: Overlapping KDE + boxplot of composite scores."""
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))

    # KDE
    ax = axes[0]
    for model, color in COLORS.items():
        data = df[df['model_key']==model]['composite_score'].dropna()
        ax.hist(data, bins=20, alpha=0.35, color=color, density=True)
        data.plot.kde(ax=ax, color=color, linewidth=2.5,
                      label=f"{'Claude' if model=='claude' else 'GPT-4o'} "
                            f"(μ={data.mean():.2f})")
        ax.axvline(data.mean(), color=color, linestyle='--', linewidth=1.5, alpha=0.8)

    ax.axvspan(1.0, 2.0, alpha=0.05, color='blue',   label='Capitalist zone')
    ax.axvspan(2.1, 3.4, alpha=0.05, color='gray',   label='Mixed zone')
    ax.axvspan(3.5, 4.4, alpha=0.05, color='orange', label='Socialist zone')
    ax.set_xlabel("Composite Ideology Score")
    ax.set_ylabel("Density")
    ax.set_title("Figure 1a: Distribution of Composite Scores")
    ax.legend(fontsize=9)
    ax.set_xlim(1, 5)

    # Boxplot
    ax = axes[1]
    data_plot = [
        df[df['model_key']=='claude']['composite_score'].dropna().values,
        df[df['model_key']=='gpt4o']['composite_score'].dropna().values,
    ]
    bp = ax.boxplot(data_plot, patch_artist=True, widths=0.5,
                    medianprops=dict(color='white', linewidth=2))
    for patch, color in zip(bp['boxes'], COLORS.values()):
        patch.set_facecolor(color)
        patch.set_alpha(0.8)

    # Add jittered points
    for i, (data, color) in enumerate(zip(data_plot, COLORS.values()), 1):
        jitter = np.random.default_rng(42).uniform(-0.15, 0.15, size=len(data))
        ax.scatter(i + jitter, data, alpha=0.15, s=8, color=color)

    ax.set_xticks([1, 2])
    ax.set_xticklabels(['Claude', 'GPT-4o'])
    ax.set_ylabel("Composite Ideology Score")
    ax.set_title("Figure 1b: Score Distribution by Model")

    # Significance bracket
    y_max = max(max(data_plot[0]), max(data_plot[1])) + 0.2
    ax.plot([1, 1, 2, 2], [y_max, y_max+0.1, y_max+0.1, y_max],
            color='black', linewidth=1)
    claude_data = df[df['model_key']=='claude']['composite_score'].dropna()
    gpt4o_data  = df[df['model_key']=='gpt4o']['composite_score'].dropna()
    _, p = mannwhitneyu(claude_data, gpt4o_data, alternative='two-sided')
    sig = "***" if p < 0.001 else ("**" if p < 0.01 else ("*" if p < 0.05 else "ns"))
    ax.text(1.5, y_max + 0.15, sig, ha='center', va='bottom', fontsize=13)

    plt.tight_layout()
    plt.savefig("fig1_composite_distributions.png")
    plt.show()
    print("Saved: fig1_composite_distributions.png")


def fig2_radar_chart(df):
    """Figure 2: Radar chart of mean dimension scores."""
    means = df.groupby('model_key')[DIMS].mean()
    categories = DIM_LABELS
    N = len(categories)
    angles = [n / float(N) * 2 * np.pi for n in range(N)]
    angles += angles[:1]

    fig, ax = plt.subplots(figsize=(7, 7), subplot_kw=dict(polar=True))

    for model, color in COLORS.items():
        values = means.loc[model].tolist()
        values += values[:1]
