    async def _one_call(prompt_id, prompt_data, run_num):
        tag = f"{model_key} | {prompt_id} | run {run_num}"

        if (model_key, prompt_id, str(run_num)) in completed:
            async with counter_lock:
                counter[0] += 1
            return

        row = {
            "timestamp":            datetime.datetime.utcnow().isoformat(),
            "model_key":            model_key,
            "model_display":        model_cfg["display"],
            "prompt_id":            prompt_id,
            "scenario_type":        prompt_data["scenario_type"],
            "framing":              prompt_data["framing"],
            "run_number":           run_num,
            "response_text":        "",
            "response_length_chars": 0,
            "tokens_in":            0,
            "tokens_out":           0,
            "error":                "",
        }

        async with sem:
            try:
                text, tok_in, tok_out = await call_model_async(model_key, prompt_data["text"])
                row["response_text"]         = text
                row["response_length_chars"] = len(text)
                row["tokens_in"]             = tok_in
                row["tokens_out"]            = tok_out
                status = f"✓ ({tok_in}in/{tok_out}out)"
            except Exception as e:
                row["error"] = str(e)[:200]
                status = f"✗ {str(e)[:60]}"

            csv_writer.write(row)

            async with counter_lock:
                counter[0] += 1
                done = counter[0]
            print(f"  [{done:>4}/{total}] {tag} {status}")

            with open(log_file, "a") as lf:
                lf.write(f"{'OK' if not row['error'] else 'ERR'} | {tag} | {row.get('error','')}\n")

            # Small delay between calls for the same model to be polite to APIs
            await asyncio.sleep(0.5)

    tasks = [
        _one_call(pid, pdata, run_num)
        for pid, pdata in prompts_to_run.items()
        for run_num in range(1, runs + 1)
    ]
    await asyncio.gather(*tasks)


