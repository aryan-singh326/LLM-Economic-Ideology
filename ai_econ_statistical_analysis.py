def run_analysis(input_file="study_data_coded.csv"):
    df = pd.read_csv(input_file)

    # Clean
    df = df[df['coding_error'].isna() | (df['coding_error'].astype(str).str.strip() == '')]
    for col in DIMS + ['composite_score']:
        df[col] = pd.to_numeric(df[col], errors='coerce')
    df = df.dropna(subset=['composite_score'])

    print(f"Loaded {len(df)} clean coded responses")
    print(f"Models: {df['model_key'].value_counts().to_dict()}")

    # Stats
    stats_results = run_tests(df)

    # Figures
    print("\n── Generating figures ──────────────────────────────────────")
    fig1_composite_distributions(df)
    fig2_radar_chart(df)
    fig3_ideology_labels(df)
    fig4_framing_effect(df)
    fig5_scenario_heatmap(df)
    fig6_dim_comparison(df)

    print("\n── All figures saved. Download with: ──────────────────────")
    print("  from google.colab import files")
    figs = ["fig1_composite_distributions.png","fig2_radar_chart.png",
            "fig3_ideology_labels.png","fig4_framing_effect.png",
            "fig5_scenario_heatmap.png","fig6_dimension_comparison.png"]
    for f in figs:
        print(f"  files.download('{f}')")

    return stats_results

