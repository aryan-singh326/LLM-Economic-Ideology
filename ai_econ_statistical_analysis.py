def cohens_d(a, b):
    pooled_std = np.sqrt((np.std(a, ddof=1)**2 + np.std(b, ddof=1)**2) / 2)
    return (np.mean(a) - np.mean(b)) / pooled_std

def interpret_d(d):
    d = abs(d)
    if d < 0.2:   return "negligible"
    elif d < 0.5: return "small"
    elif d < 0.8: return "medium"
    else:         return "large"

def run_tests(df):
    claude = df[df['model_key']=='claude']['composite_score'].dropna()
    gpt4o  = df[df['model_key']=='gpt4o']['composite_score'].dropna()

    print("\n" + "="*60)
    print("  STATISTICAL ANALYSIS REPORT")
    print("="*60)

    # ── 1. Descriptive stats ──────────────────────────────────────────────────
    print("\n── 1. Descriptive Statistics ──────────────────────────────")
    for name, series in [("Claude", claude), ("GPT-4o", gpt4o)]:
        print(f"\n  {name} (n={len(series)})")
        print(f"    Mean:     {series.mean():.3f}")
        print(f"    Median:   {series.median():.3f}")
        print(f"    Std Dev:  {series.std():.3f}")
        print(f"    Range:    {series.min():.1f} – {series.max():.1f}")
        print(f"    95% CI:   {series.mean() - 1.96*series.sem():.3f} – "
              f"{series.mean() + 1.96*series.sem():.3f}")

    # ── 2. Normality check ────────────────────────────────────────────────────
    print("\n── 2. Normality Tests (Shapiro-Wilk, sample n=50) ─────────")
    for name, series in [("Claude", claude), ("GPT-4o", gpt4o)]:
        sample = series.sample(50, random_state=42)
        stat, p = shapiro(sample)
        normal = "✓ normal" if p > 0.05 else "✗ non-normal"
        print(f"  {name}: W={stat:.3f}, p={p:.4f}  →  {normal}")

    # ── 3. Primary test: Mann-Whitney U (non-parametric) ────────────────────
    print("\n── 3. Primary Test: Mann-Whitney U ────────────────────────")
    u_stat, p_mw = mannwhitneyu(claude, gpt4o, alternative='two-sided')
    d = cohens_d(claude.values, gpt4o.values)
    print(f"  U = {u_stat:.0f},  p = {p_mw:.6f}")
    print(f"  Cohen's d = {d:.3f}  ({interpret_d(d)} effect)")
    sig = "SIGNIFICANT" if p_mw < 0.05 else "NOT significant"
    print(f"  Result: {sig} at α=0.05")

    # ── 4. Secondary test: one-way ANOVA (parametric check) ─────────────────
    print("\n── 4. Secondary Test: One-Way ANOVA ───────────────────────")
    f_stat, p_anova = f_oneway(claude, gpt4o)
    print(f"  F = {f_stat:.3f},  p = {p_anova:.6f}")
    print(f"  Result: {'SIGNIFICANT' if p_anova < 0.05 else 'NOT significant'} at α=0.05")

    # ── 5. Per-dimension tests ────────────────────────────────────────────────
    print("\n── 5. Per-Dimension Mann-Whitney U Tests ──────────────────")
    print(f"  {'Dimension':<26} {'Claude μ':>8} {'GPT-4o μ':>8} {'U':>8} {'p':>10} {'d':>7} {'sig':>5}")
    print(f"  {'─'*26} {'─'*8} {'─'*8} {'─'*8} {'─'*10} {'─'*7} {'─'*5}")
    for dim, label in zip(DIMS, DIM_LABELS):
        c = df[df['model_key']=='claude'][dim].dropna()
        g = df[df['model_key']=='gpt4o'][dim].dropna()
        u, p = mannwhitneyu(c, g, alternative='two-sided')
        d_dim = cohens_d(c.values, g.values)
        sig_mark = "***" if p < 0.001 else ("**" if p < 0.01 else ("*" if p < 0.05 else "ns"))
        print(f"  {dim:<26} {c.mean():>8.3f} {g.mean():>8.3f} {u:>8.0f} {p:>10.6f} {d_dim:>7.3f} {sig_mark:>5}")

    # ── 6. Framing effect ────────────────────────────────────────────────────
    print("\n── 6. Framing Effect (neutral vs role-based) ──────────────")
    for model in ['claude','gpt4o']:
        mdf = df[df['model_key']==model]
        neutral    = mdf[mdf['framing']=='neutral']['composite_score'].dropna()
        role_based = mdf[mdf['framing']=='role_based']['composite_score'].dropna()
        u, p = mannwhitneyu(neutral, role_based, alternative='two-sided')
        d_f = cohens_d(neutral.values, role_based.values)
        sig_mark = "***" if p < 0.001 else ("**" if p < 0.01 else ("*" if p < 0.05 else "ns"))
        print(f"  {model:<8}  neutral={neutral.mean():.3f}  role={role_based.mean():.3f}  "
              f"Δ={role_based.mean()-neutral.mean():+.3f}  p={p:.4f} {sig_mark}  d={d_f:.3f}")

    # ── 7. Scenario type breakdown ────────────────────────────────────────────
    print("\n── 7. Mean Composite Score by Scenario Type ───────────────")
    st = df.groupby(['model_key','scenario_type'])['composite_score'].mean().round(3).unstack()
    print(st.to_string())

    # ── 8. Ideology label counts ──────────────────────────────────────────────
    print("\n── 8. Ideology Label Distribution ─────────────────────────")
    counts = df.groupby(['model_key','ideology_label']).size().unstack(fill_value=0)
    pcts   = counts.div(counts.sum(axis=1), axis=0).mul(100).round(1)
    print("\n  Counts:")
    print(counts.to_string())
    print("\n  Percentages:")
    print(pcts.to_string())

    print("\n" + "="*60)
    return {"mw_p": p_mw, "cohens_d": d, "anova_p": p_anova}


