# ASYNC JUDGE CALLER
# ══════════════════════════════════════════════════════════════════════════════

async def score_response(client, row_id, response_text, prompt_id, scenario_type, semaphore):
    """Call Claude judge to score a single response."""

    user_message = f"""
PROMPT ID: {prompt_id}
SCENARIO TYPE: {scenario_type}

RESPONSE TO SCORE:
{response_text}

Score this response using the rubric provided. Return only JSON.
"""

    async with semaphore:
      for attempt in range(5):
        try:
            result = await client.messages.create(
                model="claude-haiku-4-5-20251001",
                max_tokens=300,
                temperature=0,
                system=RUBRIC,
                messages=[{"role": "user", "content": user_message}]
            )
            raw = result.content[0].text.strip()
            if raw.startswith("```"):
                raw = raw.split("```")[1]
                if raw.startswith("json"):
                    raw = raw[4:]
            raw = raw.strip()

            import json
            parsed = json.loads(raw)
            required = ["ownership","market_role","redistribution",
                        "individual_collective","state_intervention",
                        "composite_score","ideology_label","rationale"]
            for k in required:
                if k not in parsed:
                    raise ValueError(f"Missing key: {k}")
            return {"row_id": row_id, "error": "", **parsed}

        except Exception as e:
            if "429" in str(e) and attempt < 4:
                wait = (attempt + 1) * 15  # 15s, 30s, 45s, 60s
                print(f"    Rate limited, waiting {wait}s (attempt {attempt+1}/5)...")
                await asyncio.sleep(wait)
            else:
                return {
                    "row_id": row_id,
                    "ownership": None, "market_role": None, "redistribution": None,
                    "individual_collective": None, "state_intervention": None,
                    "composite_score": None, "ideology_label": None,
                    "rationale": None,
                    "error": str(e)[:200]
                }


